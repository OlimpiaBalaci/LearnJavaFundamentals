package intro_to_java1.operators;

public class RationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println(a < b);
        System.out.println(a > b);

        System.out.println(a <= b);
        System.out.println(a >= b);

        //equals
        System.out.println(a == b);
        //not-equals (!negation)
        System.out.println(a != b);
    }
}
