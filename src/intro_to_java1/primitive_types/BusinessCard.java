package intro_to_java1.primitive_types;

public class BusinessCard {
    public static void main(String[] args) {
        System.out.println("############################");
        System.out.println("#                          #");
        System.out.println("#      John Smith          #");
        System.out.println("#   Sesame Street 345/B    #");
        System.out.println("#     New York 10019       #");
        System.out.println("#                          #");
        System.out.println("############################");
        System.out.println();
        System.out.println("----------------------------");
        System.out.println("-                          -");
        System.out.println("-      John Smith          -");
        System.out.println("-   Sesame Street 345/B    -");
        System.out.println("-     New York 10019       -");
        System.out.println("-                          -");
        System.out.println("----------------------------");
    }

}
